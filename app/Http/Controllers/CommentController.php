    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $request->validate([
            'user_id' => 'required',
            'product_id' => 'required',
            'content' => 'required',
        ]);
        $comments = new Comment($request->all());
        $comments->content = $request->input('content');
        $comments->save();
        return redirect()->route('products.show', $request->input('product_id'))->with('success', 'Thêm Bình Luận Thành Công');
    }

